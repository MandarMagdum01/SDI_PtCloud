void CPropertiesWnd::UpdateProperties(std::vector<Point3D>& iPoints)
{
    // Clear existing properties
    m_wndPropList.RemoveAll();
	// Reserve space to reduce reallocations
	m_wndPropList.SetRedraw(FALSE);  // Disable redraw to optimize performance


	for (size_t i = 0; i < iPoints.size(); i++)
	{
		if (i == 100)// updating max 100 points
		{
			break;
		}

		Point3D point = iPoints[i];

		CString groupName;
		groupName.Format(_T("Point %d"), i + 1);
		CMFCPropertyGridProperty* pGroup = new CMFCPropertyGridProperty(groupName);

		// Add properties for X, Y, Z, Value 1, Value 2, Value 3, Value 4
		pGroup->AddSubItem(new CMFCPropertyGridProperty(_T("X"), (_variant_t)point.x, _T("X coordinate")));
		pGroup->AddSubItem(new CMFCPropertyGridProperty(_T("Y"), (_variant_t)point.y, _T("Y coordinate")));
		pGroup->AddSubItem(new CMFCPropertyGridProperty(_T("Z"), (_variant_t)point.z, _T("Z coordinate")));
		pGroup->AddSubItem(new CMFCPropertyGridProperty(_T("intensity"), (_variant_t)point.value1, _T("intensity")));
		pGroup->AddSubItem(new CMFCPropertyGridProperty(_T("R"), (_variant_t)point.value2, _T("Red")));
		pGroup->AddSubItem(new CMFCPropertyGridProperty(_T("G"), (_variant_t)point.value3, _T("Green")));
		pGroup->AddSubItem(new CMFCPropertyGridProperty(_T("B"), (_variant_t)point.value4, _T("Blue")));
		pGroup->Expand(0);
		// Add the group to the property list
		m_wndPropList.AddProperty(pGroup);
	
	}
	m_wndPropList.SetRedraw(TRUE);  // Re-enable redraw
	m_wndPropList.RedrawWindow();   // Force redraw to update the UI
	

}
void CPropertiesWnd::AdjustLayout()
{
	if (GetSafeHwnd () == nullptr || (AfxGetMainWnd() != nullptr && AfxGetMainWnd()->IsIconic()))
	{
		return;
	}

	CRect rectClient;
	GetClientRect(rectClient);

	int cyTlb = m_wndToolBar.CalcFixedLayout(FALSE, TRUE).cy;

	m_wndObjectCombo.SetWindowPos(nullptr, rectClient.left, rectClient.top, rectClient.Width(), m_nComboHeight, SWP_NOACTIVATE | SWP_NOZORDER);
	m_wndToolBar.SetWindowPos(nullptr, rectClient.left, rectClient.top + m_nComboHeight, rectClient.Width(), cyTlb, SWP_NOACTIVATE | SWP_NOZORDER);
	m_wndPropList.SetWindowPos(nullptr, rectClient.left, rectClient.top + m_nComboHeight + cyTlb, rectClient.Width(), rectClient.Height() -(m_nComboHeight+cyTlb), SWP_NOACTIVATE | SWP_NOZORDER);
}

int CPropertiesWnd::OnCreate(LPCREATESTRUCT lpCreateStruct)
{
	if (CDockablePane::OnCreate(lpCreateStruct) == -1)
		return -1;

	CRect rectDummy;
	rectDummy.SetRectEmpty();

	// Create combo:
	const DWORD dwViewStyle = WS_CHILD | WS_VISIBLE | CBS_DROPDOWNLIST | WS_BORDER | CBS_SORT | WS_CLIPSIBLINGS | WS_CLIPCHILDREN;

	if (!m_wndObjectCombo.Create(dwViewStyle, rectDummy, this, 1))
	{
		TRACE0("Failed to create Properties Combo \n");
		return -1;      // fail to create
	}

	m_wndObjectCombo.AddString(_T("Application"));
	m_wndObjectCombo.AddString(_T("Properties Window"));
	m_wndObjectCombo.SetCurSel(0);

	CRect rectCombo;
	m_wndObjectCombo.GetClientRect (&rectCombo);

	m_nComboHeight = rectCombo.Height();

	if (!m_wndPropList.Create(WS_VISIBLE | WS_CHILD, rectDummy, this, 2))
	{
		TRACE0("Failed to create Properties Grid \n");
		return -1;      // fail to create
	}

	InitPropList();

	m_wndToolBar.Create(this, AFX_DEFAULT_TOOLBAR_STYLE, IDR_PROPERTIES);
	m_wndToolBar.LoadToolBar(IDR_PROPERTIES, 0, 0, TRUE /* Is locked */);
	m_wndToolBar.CleanUpLockedImages();
	m_wndToolBar.LoadBitmap(theApp.m_bHiColorIcons ? IDB_PROPERTIES_HC : IDR_PROPERTIES, 0, 0, TRUE /* Locked */);

	m_wndToolBar.SetPaneStyle(m_wndToolBar.GetPaneStyle() | CBRS_TOOLTIPS | CBRS_FLYBY);
	m_wndToolBar.SetPaneStyle(m_wndToolBar.GetPaneStyle() & ~(CBRS_GRIPPER | CBRS_SIZE_DYNAMIC | CBRS_BORDER_TOP | CBRS_BORDER_BOTTOM | CBRS_BORDER_LEFT | CBRS_BORDER_RIGHT));
	m_wndToolBar.SetOwner(this);

	// All commands will be routed via this control , not via the parent frame:
	m_wndToolBar.SetRouteCommandsViaFrame(FALSE);

	AdjustLayout();
	return 0;
}

